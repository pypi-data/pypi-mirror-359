import click
import hcs_core.sglib.cli_options as cli
from hcs_core.ctxp import profile
from hcs_core.util import pki_util

from hcs_cli.service.pki import certificate


def _write_file(file_path: str, text: str):
    with open(file_path, "w") as file:
        file.write(text)


@click.command()
@click.argument("resource-name", type=str, required=True)
@click.option(
    "--key-length",
    type=int,
    default=2048,
    required=False,
    help="Private key length",
)
@click.option(
    "--validity-in-days",
    type=int,
    default=365,
    required=False,
    help="Days before the certificate expires",
)
@cli.org_id
def sign_resource_cert(resource_name: str, key_length: int, org: str, validity_in_days: int = 365):
    """Create certificate for a specific resource. This command will generate cert chain and private key file stored in the current directory."""
    org_id = cli.get_org_id(org)
    csr_pem, private_key_pem = pki_util.generate_CSR(resource_name, key_length=key_length)
    ret = certificate.sign_resource_cert(org_id, csr_pem, validity_in_days)

    long_name = f"{profile.name()}-{org_id}-{resource_name}"
    key_file = long_name + ".key"
    print("Private key (generated by CLI): " + key_file)
    _write_file(key_file, private_key_pem)

    client_cert_chain_file = long_name + ".crt"
    print("Signed resource certificate with chain (response from server): " + client_cert_chain_file)
    _write_file(client_cert_chain_file, ret)
